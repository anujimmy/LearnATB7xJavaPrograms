package ex_21072024_LoopsNConditions;

public class Lab118 {
    public static void main(String[] args) {
        for (int i = 0; i < 10 ; i++) { // 0 to 9 -> 10 times
            System.out.println(i+ "Noel");
        }

        for (int i = 1; i <= 10 ; i++) { // 1 to 10 - > 10 times
            System.out.println(i+"thomas");
        }
    }
}
