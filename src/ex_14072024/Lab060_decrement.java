package ex_14072024;

public class Lab060_decrement {
    public static void main(String[] args) {
        int b = 10;
        int c = b--;
        System.out.println(c);
        System.out.println(b);
        System.out.println(b--);
        System.out.println(c);
        System.out.println(--b);
    }
}
