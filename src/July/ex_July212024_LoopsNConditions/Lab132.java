package July.ex_July212024_LoopsNConditions;

public class Lab132 {
    public static void main(String[] args) {
        int i =10;
        while(i<10){
            System.out.println("inside while");
            System.out.println(i);
        }

        do{
            System.out.println("inside do while");
            System.out.println(i);
        }while(i<10);
    }
}
