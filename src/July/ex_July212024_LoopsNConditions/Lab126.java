public class Lab126 {
    public static void main(String[] args) {
        // Loops - while & dowhile
        // while - ABC
        /*A - Initiation
         * B - Condition
         * C - Increment/decrement */
        int i = 0; // A
        while(i<=10){ //B
            System.out.println(i);
            i++; //C
        }
    }

}
